export class TableRowProperties {
    constructor(tableRowName, tableRowWidth, tableRowHeight,
        borderWidth, borderColor, numberOfCells
    )
    {
        this.tableRowName = tableRowName;
        this.tableRowWidth = tableRowWidth;
        this.tableRowHeight = tableRowHeight;
        this.borderWidth = borderWidth;
        this.borderColor = borderColor;
        this.numberOfCells = numberOfCells;
    }
}